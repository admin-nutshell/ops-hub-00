#!/usr/bin/env python3
"""
gen-live-config.py — generalize T-88's corrected live-run config generator (T-89, ADR-0007 §6 step 1).

WHY THIS EXISTS
---------------
The three product evals (evals/ticket-triage.yaml, evals/ticket-respond.yaml,
evals/kb-learn.yaml) pin `anthropic:claude-sonnet-4-6` as their PROMPT-CONTRACT
reference provider. That block is for CI schema validation (`promptfoo validate`)
and prompt-authoring stability — it is NOT the model these prompts run against in
production. Production routes each prompt through a LiteLLM alias over an
OpenAI-compatible `/v1` endpoint.

This generator reads one of those eval files UNCHANGED and emits a *separate*,
self-contained live-run config that does the three things the schema check
deliberately does not (ADR-0007 §2):

  1. PROVIDER SWAP  — repoint the provider at `openai:chat:<target-alias>` against
                      the LiteLLM `/v1` endpoint (target model = real production model).
  2. SYSTEM PROMPT AS A REAL SYSTEM-ROLE MESSAGE — the load-bearing T-84/T-88 fix.
                      The openai-compatible provider SILENTLY IGNORES `config.system`
                      (unlike the anthropic provider the eval file pins). A system
                      prompt left in `config.system` is dropped and the model runs
                      with ZERO instructions — that was the real bug behind run
                      28997069750's meaningless 25% (497 total prompt tokens across
                      4 calls => ~124/call = user message only, none of the ~850-token
                      system prompt). This generator moves the system prompt OUT of
                      config and delivers it as a real `{role:"system"}` message via a
                      prompt-FUNCTION file. Encoding it here ONCE means no future
                      per-eval override can reintroduce the `config.system` class of bug.
  3. GRADER THROUGH LITELLM — the llm-rubric grader routes through LiteLLM too
                      (provider-neutral, CLAUDE.md standing constraint), pointed at
                      `openai:chat:<judge-alias>`. The judge alias is a SEPARATE
                      parameter from the target alias so a caller (T-91/T-92) can set
                      grader != target (ADR §5.3). This script does not ENFORCE
                      grader != target — it only makes the parameter exist (T-91's job).

It does NOT mutate the source eval file. It writes a self-contained `<name>-live.yaml`
plus a `<name>-prompt.mjs` prompt function into the chosen out-dir.

Generic across all three evals by construction: it reads providers[0].config
(preserving temperature / max_tokens / any other tuning key, only popping `system`),
and rebuilds prompts[0]'s templated user message from the eval file itself, so it
cannot drift from the source.

USAGE
-----
  python3 scripts/eval/gen-live-config.py \
      --eval-file evals/kb-learn.yaml \
      --target-alias triage-model \
      --judge-alias  triage-model \
      --litellm-base https://litellm-staging.inatechshell.ca/v1 \
      --out-dir /tmp/eval-live

Emits (basename derived from the eval file):
  <out-dir>/kb-learn-live.yaml       — the self-contained live-run promptfoo config
  <out-dir>/kb-learn-prompt.mjs      — the system+user chat prompt function
"""

import argparse
import json
import os
import re
import sys

import yaml


def build_prompt_function(user_template: str, system_prompt: str) -> str:
    """Emit a JS prompt-function that returns a [system, user] chat message array.

    A FUNCTION (not an inline JSON chat string) is deliberate: it builds the
    messages from the raw var values in JS, so a ticket body containing a
    double-quote or newline can't break a JSON.parse the way an interpolate-
    then-parse chat string would.

    - `system_prompt` is embedded via json.dumps (a valid JS string literal) so it
      stays byte-identical to the eval file's `system:` block.
    - `user_template` is the eval's prompts[0] with promptfoo's {{var}} placeholders
      rewritten to `${vars.var ?? ''}` so the JS template literal fills them in.
    """
    user_js = re.sub(r"\{\{\s*(\w+)\s*\}\}", r"${vars.\1 ?? ''}", user_template)
    return (
        "// AUTO-GENERATED by scripts/eval/gen-live-config.py (T-89). Do not edit by hand.\n"
        "// Delivers the eval's system prompt as a REAL {role:'system'} message so the\n"
        "// openai-compatible LiteLLM provider actually receives it (it ignores config.system).\n"
        "const SYSTEM = " + json.dumps(system_prompt) + ";\n"
        "export default function ({ vars }) {\n"
        "  const user = `" + user_js + "`;\n"
        "  return [\n"
        "    { role: 'system', content: SYSTEM },\n"
        "    { role: 'user', content: user },\n"
        "  ];\n"
        "}\n"
    )


def generate(eval_file: str, target_alias: str, judge_alias: str,
             litellm_base: str, out_dir: str) -> tuple[str, str]:
    with open(eval_file, encoding="utf-8") as f:
        cfg = yaml.safe_load(f)

    if not cfg.get("providers"):
        sys.exit(f"::error::{eval_file} has no providers block")
    if not cfg.get("prompts"):
        sys.exit(f"::error::{eval_file} has no prompts block")

    # TARGET provider: preserve every tuning key (temperature, max_tokens, ...),
    # point at LiteLLM, and move the system prompt OUT of config (it is inert once
    # the provider is swapped anthropic -> openai — see module docstring #2).
    original_provider = cfg["providers"][0]
    target_config = dict(original_provider.get("config", {}))
    system_prompt = target_config.pop("system", "")
    target_config["apiBaseUrl"] = litellm_base
    cfg["providers"] = [{"id": f"openai:chat:{target_alias}", "config": target_config}]

    # Rebuild prompts[0] as a system+user chat array via a prompt-function file,
    # derived from the eval file itself so it cannot drift from the source contract.
    user_template = cfg["prompts"][0].rstrip("\n")
    prompt_fn = build_prompt_function(user_template, system_prompt)

    os.makedirs(out_dir, exist_ok=True)
    basename = os.path.splitext(os.path.basename(eval_file))[0]
    mjs_path = os.path.join(out_dir, f"{basename}-prompt.mjs")
    yaml_path = os.path.join(out_dir, f"{basename}-live.yaml")

    with open(mjs_path, "w", encoding="utf-8") as f:
        f.write(prompt_fn)
    # promptfoo resolves a file:// URI with an absolute path. abspath keeps this
    # correct whether out-dir is /tmp in CI or a local scratchpad path.
    cfg["prompts"] = ["file://" + os.path.abspath(mjs_path).replace(os.sep, "/")]

    # GRADER provider: deliberately NOT the target's system prompt — the grader
    # answers a different question (does this output satisfy rubric X). It gets a
    # clean provider pointed at the same LiteLLM instance, on the JUDGE alias so a
    # caller can set grader != target (ADR §5.3; enforcement is T-91, not here).
    grader_provider = {"id": f"openai:chat:{judge_alias}", "config": {"apiBaseUrl": litellm_base}}
    cfg["defaultTest"] = {"options": {"provider": grader_provider}}

    with open(yaml_path, "w", encoding="utf-8") as f:
        yaml.safe_dump(cfg, f, sort_keys=False, allow_unicode=True)

    return yaml_path, mjs_path


def main() -> None:
    p = argparse.ArgumentParser(description="Generate a self-contained live-run promptfoo config from a product eval file.")
    p.add_argument("--eval-file", required=True, help="Path to an evals/*.yaml source file (unchanged).")
    p.add_argument("--target-alias", required=True, help="LiteLLM alias for the model UNDER TEST, e.g. triage-model.")
    p.add_argument("--judge-alias", required=True, help="LiteLLM alias for the llm-rubric GRADER (may differ from target).")
    p.add_argument("--litellm-base", default="https://litellm-staging.inatechshell.ca/v1",
                   help="LiteLLM OpenAI-compatible /v1 base URL.")
    p.add_argument("--out-dir", default="/tmp/eval-live", help="Directory for the generated live config + prompt function.")
    args = p.parse_args()

    yaml_path, mjs_path = generate(
        args.eval_file, args.target_alias, args.judge_alias, args.litellm_base, args.out_dir
    )

    with open(mjs_path, encoding="utf-8") as f:
        prompt_fn = f.read()
    # SYSTEM literal length, extracted from the generated file for the run log —
    # this is the same signal the §5.1 token-count guard (T-91) will assert on.
    m = re.search(r"const SYSTEM = (.*?);\nexport default", prompt_fn, re.DOTALL)
    sys_len = len(json.loads(m.group(1))) if m else 0

    print(f"Generated {yaml_path} + {mjs_path}")
    print(f"  eval-file    : {args.eval_file}")
    print(f"  target alias : openai:chat:{args.target_alias}")
    print(f"  judge alias  : openai:chat:{args.judge_alias}")
    print(f"  system prompt length (chars): {sys_len}")
    print("--- prompt function (head) ---")
    print(prompt_fn[:600])
    with open(yaml_path, encoding="utf-8") as f:
        live = yaml.safe_load(f)
    print("--- live config (providers + prompts + defaultTest) ---")
    print(yaml.safe_dump(
        {"providers": live["providers"], "prompts": live["prompts"], "defaultTest": live["defaultTest"]},
        sort_keys=False, allow_unicode=True,
    ))


if __name__ == "__main__":
    main()
